# Concatenate three strings: your name, hobby, and favorite color, and print them.

def print_sentence(name, hobby, favorite_color):
    print(str(name), "hobby is", str(hobby), "and his favorite color is", str(favorite_color))

name = "Renz"
hobby = "watching movies"
favorite_color = "red"

print_sentence(name, hobby, favorite_color)
