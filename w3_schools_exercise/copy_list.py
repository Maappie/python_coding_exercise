# use copy method to copy list
fruits = [ "Orange", "Kiwi", "cherry", "orange", "kiwi", "banana"]
new_fruits = fruits.copy()

print(new_fruits)

#use list method to create a copy of a list
fruits = [ "Orange", "Kiwi", "cherry", "orange", "kiwi", "banana"]
new_fruits = list(fruits)

print(new_fruits)

# use slice method to copy
fruits = [ "Orange", "Kiwi", "cherry", "orange", "kiwi", "banana"]
new_fruits = fruits[:]

print(new_fruits)


