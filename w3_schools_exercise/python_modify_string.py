# uppercase string
word = "Pamantasan ng Lungsod ng Maynila"
print(word.upper())

# lowercase string
word = "Pamantasan ng Lungsod ng Maynila"
print(word.lower())

# capitalize a string
word = "pamantasan ng lungsod ng maynila"
print(word.capitalize())

# capitilaze every first letter of a word
word = "pamantasan ng lungsod ng maynila"
print(word.title())

# Remove Whitespace from beginning or end
word = "  Pamantasan ng Lungsod ng Maynila"
print(word.strip())

# replace string
word = "Pamantasan ng Lungsod ng Maynila"
print(word.replace("Maynila", "Makati"))

# split string
word = "Pamantasan ng Lungsod ng Maynila"
print(word.split(" "))


