# string slicing 
# Get the characters from position 2 to position 5 (not included):
word = "Hello, World!"
print(word[2:5])
# slice from the start
word = "Pamantasan ng lungsod ng Maynila"
print(word[:5])
# slice from the end
word = "Pamantasan ng lungsod ng Maynila"
print(word[5:])
# negative indexing
word = "Pamantasan ng lungsod ng Maynila"
print(word[-5:-2])

